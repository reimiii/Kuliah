package tugas.ekstensi.pertemuan.duabelas;

public class PraktikumForBersarang {
    public static void main(String[] args) {
        int i;
        for (i = 1; i <= 6; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(i + " ");
            }

            System.out.println();
        }
    }
}
