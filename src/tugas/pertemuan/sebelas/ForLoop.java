package tugas.pertemuan.sebelas;

public class ForLoop {
    public static void main(String[] args) {
        System.out.println("Pengulangan dengan for arah index naik");
        for (int i = 1; i <= 5; ++i) {
            System.out.println("Index naik ke: " + i);
        }

        System.out.println("Pengulangan dengan for arah index naik");
        for (int i = 5; i > 0; i--) {
            System.out.println("Index naik ke: " + i);
        }
    }
}
